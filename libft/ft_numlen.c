#include "libft.h"

int		ft_numlen(int n)
{
	int i;

	i = 1;
	if (n < 0)
		n *= (-1);
	else if (n > 0)
		i = 0;
	while (n)
	{
		n /= 10;
		i++;
	}
	return (i);
}
